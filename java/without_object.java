//without creating object doing program by methods in java

public class without_object {

	public static void main(String[] args) {
		int L = 5;
		int B = 5;
		int H = 5;
		System.out.println("hello");
		hustler(L,B,H);
		
}
	static void hustler(int KK,int SL,int MK)
	{
		int volume = KK * SL * MK;
		System.out.println(volume);
	
	}
}