import java.util.*;
 class pattern_square {
    public static void main(String[] args)  
    {
Scanner obj = new Scanner(System.in);
   int row;
   int col;
    int num = obj.nextInt();
    for (row=1;row<=num;row++)
    {
       
        System.out.print("\n");

    for (col=1;col<=row;col++)
    {
        
        System.out.print("*");
       
    }
   
}
}
    }
    
