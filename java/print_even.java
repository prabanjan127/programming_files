import java.util.*;
class print_even {
    public static void main(String[] args) {
    Scanner obj = new Scanner(System.in);
    {
        int n = obj.nextInt();
        for(int i=1;i<n;i++)
        {
            if(i%2==1)
            {
             System.out.println(i);
             continue;
            }

        }
    
    }

    
}
}
